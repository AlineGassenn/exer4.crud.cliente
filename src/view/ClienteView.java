    private void jTableClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClienteMouseClicked
        // TODO add your handling code here:
        if(jTableCliente.getSelectedRow()!=0){
            jTextFieldNome.setText((String) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 1).toString());
            jTextFieldEmail.setText((String) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 2).toString());
            jTextFieldCpf.setText((String) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 3).toString());
            jTextFieldTelefone.setText((String) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 4).toString());
            jTextFieldDataAniversario.setText((String) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 5).toString());
            
        }
    }//GEN-LAST:event_jTableClienteMouseClicked

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        // TODO add your handling code here:
         if(jTableCliente.getSelectedRow()!= -1){
             clienteController.update((int) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 0),
                     jTextFieldNome.getText(),
                     jTextFieldEmail.getText(),
                     jTextFieldCpf.getText(),
                     jTextFieldTelefone.getText(),
                     jTextFieldDataAniversario.getText());
             
             this.getListaCliente();
             limpaCampos();
             }
          
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        if(jTableCliente.getSelectedRow()!= -1){
         clienteController.delete ((int) jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 0));
         
         this.limpaCampos();
         this.getListaCliente();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente para remover");
        }
        
        
    }//GEN-LAST:event_jButtonExcluirActionPerformed

