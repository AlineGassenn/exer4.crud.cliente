    public void getListaCliente(){
         DefaultTableModel modeloTableCliente = (DefaultTableModel) jTableCliente.getModel();
         modeloTableCliente.setNumRows(0);
         
         for(Cliente c : clienteController.read()) {
             modeloTableCliente.addRow(new Object[]{
             c.getIdcliente(),
             c.getNome(),
             c.getEmail(),
             c.getTelefone(),
             c.getDataaniversario()});
         
             
         }
    }
    
    public void limpaCampos(){
        
        jTextFieldNome.setText("");
        jTextFieldEmail.setText("");
        jTextFieldCpf.setText("");
        jTextFieldTelefone.setText("");
        jTextFieldDataAniversario.setText("");
        jTextFieldNome.requestFocus();
        
    }

